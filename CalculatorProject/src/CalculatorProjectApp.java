import java.util.Scanner;

public class CalculatorProjectApp {

	public static void main(String[] args) {
		
		System.out.println("Welcome to my Calculator App");
	//declare a scanner
		Scanner sc = new Scanner(System.in);
	//define two ints
		System.out.print("Enter Int A:   ");
		int a = sc.nextInt(); 
		System.out.print("Enter Int B:   ");
		int b = sc.nextInt();
		
	//define, sum, diff, product quotient
		int sum = a + b;
		int diff = a - b;
		int product = a * b;
		double quotient = a / b;
		
	// print to screen
		System.out.println();
		System.out.println("Sum:       a + b = " + sum);
		System.out.println("Differnce: a - b = " + diff);
		System.out.println("Product:   a * b = " + product);
		System.out.println("Product:   a / b = " + quotient);
		
		System.out.println();
		System.out.println("Goodbye!");
		
		

	}

}
